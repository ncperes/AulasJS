const num1 = 10;
const num2 = parseInt('5');
console.log(num1 + num2);

const num3 = 10;
const num4 = parseFloat('5.2');
console.log(num3 + num4);

const num5 = 10;
const num6 = Number('5.2');
console.log(num5 + num6);

// parseInt converte para 'inteiro'
// parseFloat converte para 'decimais'
// Number converte de qualquer forma